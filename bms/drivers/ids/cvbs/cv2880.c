static int cv2880_open(void)
{
	cvbs_dbg("%s\n", __func__);
	/* init i2c */
	i2c_dev = itcs_i2c_init(CVBS_IIC_INDEX, I2C_MASTER, CVBS_IIC_ADDRMODE, CVBS_IIC_ADDR, CVBS_IIC_SPEED);
	if(i2c_dev == NULL){
		printf("i2c initialize failed.\n");
		return -1;
	}

	/* reset cv2880 */
	itcs_pads_set_mode(CPU, CVBS_RESET_PIN, PADS_MODE_OUT, 1);
	itcs_pads_set_value(CPU, CVBS_RESET_PIN, 0);
	udelay(100000);
	itcs_pads_set_value(CPU, CVBS_RESET_PIN, 1);

	return 0;
}

static int cv2880_close(void)
{
	cvbs_dbg("%s\n", __func__);
	itcs_pads_set_value(CPU, CVBS_RESET_PIN, 0);
	udelay(500000);
	return 0;
}

int cv2880_init(void)
{
	struct cvbs_dev *cv2880;	

	cv2880 = (struct cvbs_dev *)malloc(sizeof(struct cvbs_dev));
	if (!cv2880)
		return -1;

	cv2880->name = "cv2880";
	cv2880->config = cv2880_config;
	cv2880->open = cv2880_open;
	cv2880->close = cv2880_close;

	return cvbs_register(cv2880);
}
