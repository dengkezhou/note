#include "mmc-wrapper.h"

/* Definitions of physical drive number for each drive */
#define DEV_MMC0		0
#define DEV_MMC1		1


/*-----------------------------------------------------------------------*/
/* Get Drive Status                                                      */
/*-----------------------------------------------------------------------*/

DSTATUS disk_status (
	BYTE pdrv		/* Physical drive nmuber to identify the drive */
)
{
	DSTATUS stat;
	int result;

	switch (pdrv) {
	case DEV_MMC0 :
	case DEV_MMC1 :
		result = MMC_disk_status(pdrv);

		// translate the reslut code here
		stat = result ? STA_NOINIT : 0;

		return stat;
	}
	return STA_NOINIT;
}



/*-----------------------------------------------------------------------*/
/* Inidialize a Drive                                                    */
/*-----------------------------------------------------------------------*/

DSTATUS disk_initialize (
	BYTE pdrv				/* Physical drive nmuber to identify the drive */
)
{
	DSTATUS stat;
	int result;

	switch (pdrv) {
	case DEV_MMC0 :
	case DEV_MMC1 :
		result = MMC_disk_initialize(pdrv);

		// translate the reslut code here

		stat = result ? STA_NOINIT : 0;

		return stat;
	}
	return STA_NOINIT;
}



/*-----------------------------------------------------------------------*/
/* Read Sector(s)                                                        */
/*-----------------------------------------------------------------------*/

DRESULT disk_read (
	BYTE pdrv,		/* Physical drive nmuber to identify the drive */
	BYTE *buff,		/* Data buffer to store read data */
	LBA_t sector,	/* Start sector in LBA */
	UINT count		/* Number of sectors to read */
)
{
	DRESULT res;
	int result;

	switch (pdrv) {
	case DEV_MMC0 :
	case DEV_MMC1 :
		// translate the arguments here

		result = MMC_disk_read(pdrv, buff, sector, count);

		// translate the reslut code here
		res = result ? RES_ERROR : RES_OK;

		return res;
	}

	return RES_PARERR;
}



